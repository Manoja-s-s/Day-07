class relationalOperatorsDemonstration {

	public static void main(String[] args) {
		System.out.println(5 < 10); // < less then Operators
		System.out.println(10 > 5); // < greater then Operators
		System.out.println(10 <= 5); // < less then or equal to Operators
		System.out.println(5 >= 10); // < greater then or equal to Operators
		System.out.println(5 == 5); // equal to Operators ( Checks Left value is Equal to right value )
		System.out.println(10 != 5); // Not equal to Operators
				
	}

}