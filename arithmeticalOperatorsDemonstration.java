class arithmeticalOperatorsDemonstration {

	public static void main(String[] args) {
		var a = 10;
		var b = 5;
		var addResult = a + b;
		var subResult = a - b;
		var mulResult = a * b;
		var divResult = a / b;
		System.out.println("Sum: " + addResult);
		System.out.println("Differenc: " + subResult);
		System.out.println("Product: " + mulResult);
		System.out.println("Quotient: " + divResult);
	}

}
